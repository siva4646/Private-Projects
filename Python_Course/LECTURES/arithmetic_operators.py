i = 10
print(i / 3) # 3.33333333333
print(i // 3) # 3
print(i % 3) # 1
print(i ** 3) # 1000
print(i + 3) # 10
print(i - 3) # 7

# /: Division: Divides the value by n
# //: Floor Division: Divides the number by n and rounds it to the nearest whole number.
# %: Modulato: Divides the integer by n and prints the remainder.
# **: Pow / Power: Raises the number to n power.
# + and -: Self-Explanatory.

x = 10
x = x + 3 # Sets x to x + 3
# Optimized:
x += 3 # Increments x by 3 (x = x + 3)

print(x) # 16